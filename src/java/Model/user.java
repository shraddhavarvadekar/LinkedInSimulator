package Model;

/**
 *
 * @author meghana garnepudi
 */
public class user {
    
    String id ;
    String name ;
    String company ;
    String type ;
    String email;
    
    public user ( String pid , String pname , String pcompany , String ptype , String pemail )
    {
        id = pid;
        name = pname ;
        company = pcompany;
        type = ptype ;
        email = pemail;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
}
